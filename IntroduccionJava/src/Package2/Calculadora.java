package Package2;

public class Calculadora
{
    public double Sumar(double a, double b)
    {
        double res = a + b;
        return res;
    };

    public double Restar(double a, double b)
    {
        double res = a - b;
        return res;
    };

    public double Dividir (double a, double b)
    {
        double res = a/b;
        return res;
    };

    public double Multipilcar (double a, double b)
    {
        double res = a*b;
        return res;
    };
}
